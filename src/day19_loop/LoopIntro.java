package day19_loop;

public class LoopIntro {

    public static void main(String[] args) {



        for(int i=1; i<=10; i++){
            //initialization; condition; iterator
            System.out.println("hello");
        }

        System.out.println("==============================");
        for(int n=5; n<=10; n++) {
            System.out.println("Gubek");
        }

        System.out.println("===============================");
        for(int m=5; m>0; m--){
            System.out.println("name");
        }

        System.out.println("===============================");
    }

}
