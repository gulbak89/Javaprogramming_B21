package day21_whileLoop;

public class WhileLoop {

    public static void main(String[] args) {

        for(int i=1; i<=10; i++) {                           // "while loop" == "repeated if statement"

            System.out.print(i);

        }
        System.out.println();

        System.out.println("========================");

        int i=1;
        while(i<=10){

            System.out.print(i);
            i++;
        }





    }
}
