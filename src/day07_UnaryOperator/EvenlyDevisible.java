package day07_UnaryOperator;

public class EvenlyDevisible {

    public static void main(String[] args) {

        int num = 30;

        boolean isDivisibleBy2 = num%2 <1;
        boolean isDivisibleBy3 = num%3 <1;
        boolean isDivisibleBy5 = num%5 <1;





        System.out.println(num+" is evenly divisible by 2 is "+isDivisibleBy2);
        System.out.println(num+" is evenly divisible by 3 is "+isDivisibleBy3);
        System.out.println(num+" is evenly divisible by 5 is "+isDivisibleBy5);

    }
}
