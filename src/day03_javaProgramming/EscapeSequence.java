package day03_javaProgramming;

public class EscapeSequence {

    public static void main(String[] args) {
        System.out.println("Hello everyone");
        System.out.println("My name is Gubek");


        System.out.println("Hello everyone \n My name is Gubak" );
        System.out.println("\t hello \n my name is gubbek");

        System.out.println("/"); //print single front slash
        System.out.println("\\"); //print single back slash

        System.out.println("my anme is \'Gubek\'");  //  \' gives single quote
        System.out.println("my name is \" gubek\""); //  \" gives double quote






    }
}


